        <img style="float: left; margin-top: 5px; margin-right: 20px; margin-bottom: 5px; margin-left: 5px; border-top-width: 0px; border-right-width: 0px; border-bottom-width: 0px; border-left-width: 0px;" width="350" height="178" alt="" class="art-lightbox" src="../images/1e24fc92-d7a8-4585-a632-60267c515d4b.png">
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">By David Carr&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">AUSTIN, Tex. — In the movie “Tiny Furniture,” a young woman named Aura breaks up with her boyfriend, returns home from college, moves in with her wildly successful artist family in a pristine TriBeCa loft, all the while trying to find a place to stand in the world.</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">That’s also the story, more or less, of Lena Dunham, 23, who wrote, directed, and played the lead role in the film, which sounds like a senior project gone amok, except that “Tiny Furniture” won the juried narrative film prize — essentially the best feature film — at the South by Southwest Music and Media Conference here.</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">In the film, her mom and sister, played by her mom and sister, can’t muster a lot of warmth when she comes home to a shiny, white-on-white apartment where everything seems hidden in a large white cabinet full of endless doors. As in a Cheever novel, secrets and emotional history lie behind seemingly quiet facades.</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">A 23-year-old’s winning big kudos for a movie that was made for under six figures, about as much as a high-end BMW, is story enough, but there’s more: Ms. Dunham came to SXSW, as the festival is known, in 2009 with a rough but promising first feature and met two people who would become producers of her new film, as well as its director of photography, editor and a lead actor.</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">Talking while she was riding downtown for yet another showing, Janet Pierson, head of the film festival, could not hide her pride.</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">“I loved her first film, but when I saw this one, my jaw dropped,” she said. “It was so original, so smart, so funny. Lena came here last year and had this creative experience, met all these people and then turned around and came back with a film that was just a huge leap forward.”</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">Ms. Dunham is a keen writer, creating angular, quietly weaponized dialogue that her characters use to maim one another.</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">The film’s title, “Tiny Furniture,” derives from the art of the mother character, played by Laurie Simmons, who in real life is a very successful fine-art photographer whose work often involves dolls and dioramas and who is married to the painter Carroll Dunham. In Ms. Dunham’s film, Aura’s mother makes photographs by moving teeny pieces of furniture around while juxtaposing them with larger human elements that don’t seem to fit.</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">Although the movie came together in a matter of months, it looks anything but thrown together, partly because Jody Lee Lipes, the director of photography, who has his own film here called “NY Export: Opus Jazz,” did not let the lack of financing show up on screen. And while Ms. Dunham’s film may reflect the quandaries of a 23-year-old, there is nothing juvenile about its execution. “Tiny Furniture” brings to mind Larry David’s ability to take his own tics and add humor and stakes to make them matter to others.</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">In the story, Aura returns home from college after being dumped by her boyfriend at the Burning Man festival. Confronted with the specter of a hyper-achieving mother who is riveted by her work and a sister who is charging along the same path, Aura examines her assets: a worthless film theory degree, an embarrassing YouTube movie that has 357 hits and a penchant for fecklessness around men. After taking a job as a hostess at a restaurant, she meets two men, both narcissistic jerks, albeit in very different ways, who value only what they can take from her.</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">But one of the things neither seems to want from her is intimacy, or even a casual hook-up. All of which leaves her wondering not just who she is supposed to be, but what she is worth.</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">Professionally, all of that stuff seems pretty far away. Sitting in the Driskill Hotel here with the posse that made the film, Ms. Dunham decided to go really nuts after a day of agent meetings, the announcement of her award and a film festival closing party. She ordered nachos.</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">“I am a macrobiotic vegan, so this is a big thing that I’m having these,” she said.</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">She wrote “Tiny Furniture” in a four-day fever in October, shot the film in November and after getting a waiver for a late submission, sent it to SXSW in January.</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">“The movie came out pretty true to form,” she said. “It’s about a period when someone doesn’t know how to value yourself. She is no longer a student, but not defined by a career yet, she is not defined by relationships, or by being someone’s child.”</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">Privilege has its advantages, but it doesn’t solve the basic math of becoming who you are supposed to be. Ms. Dunham grew up in SoHo, went to St. Ann’s School in Brooklyn and graduated from Oberlin College with a degree in creative writing in 2008. She’s spent a great deal of time creating two Web comedy series, “Tight Shots," and “Delusional Downtown Divas.”</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">As the person who plays the person who is having trouble meeting life on life’s terms and who flings herself against indifferent men, her exposure in the film is significant.</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">“I have no problem with that,” she said. “I am a little bit of a sad sack when it comes to romance. I don’t really have a lot of vanity and I find a real pleasure in the acting.”</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">One of her producers, Alicia Van Couvering, slid into the booth and said that it was impressive to watch Ms. Dunham work.</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">“She would do a scene, be completely in character, and then we’d cut, and she’d say, ‘That was really great, but the readings of lines three and eight were a little off, so let’s try it one more time.’ She knows exactly what she wants.”</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">As it turned out, this actor-writer-director wanted some ice cream, another transgressive reward at the end of a big day. She was still picking at it, guiltily, when the check came and was patting her pockets looking for money to pay her share. Ms. Van Couvering insisted on picking up Ms. Dunham’s part of the tab. Ms. Dunham continued to protest.</span></p>
        <p>         </p>
        <p><span style="font-family: 'Palatino Linotype'; font-size: 16px;">&nbsp;</span></p>
        <p>         </p>
        <p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">“Lena, you made a hit movie,” Ms. Van Couvering said. “We can pay for your nachos.”</span><div class="fb-like" data-href="https://developers.facebook.com/docs/plugins/" data-layout="standard" data-action="like" data-show-faces="true" data-share="true"></div>

