<p>&nbsp;<span style="font-family: 'Palatino Linotype'; font-size: 16px;">Lena Dunham writes, directs and stars as Aura, the girl who really wants you to know that she is having a very, very hard time. Lena's mother, photographer Laurie Simmons, plays the fictional mother of Aura, and Dunham's precocious sister Grace Dunham plays Nadine, Aura's precocious sister. Alex Karpovsky and David Call are two very different but equally humiliating romantic interests; Jemima Kirke and Merritt Wever are Aura's diametrically opposed friends. This is Dunham's second feature film; the first, Creative Nonfiction, premiered at SXSW in March 2009.</span></p>
