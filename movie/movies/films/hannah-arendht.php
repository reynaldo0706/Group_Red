<p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">Hannah Arendt is a 2012 German-Luxembourgian-French biographical drama film about German-Jewish philosopher and political theorist Hannah Arendt directed by Margarethe von Trotta and starring Barbara Sukowa. It is distributed by Zeitgeist Films in the United States, where it opened theatrically on 29 May 2013</span></p>
<p class="MsoNormal"><span style="font-family: 'Palatino Linotype'; font-size: 16px;">In the award-winning HANNAH ARENDT, the sublime Barbara Sukowa reteams with director Margarethe von Trotta (Vision, Rosa Luxemburg) for a brilliant new biopic of the influential German-Jewish philosopher and political theorist. Arendt’s reporting on the 1961 trial of ex-Nazi Adolf Eichmann in The New Yorker—controversial both for her portrayal of Eichmann and the Jewish councils—introduced her now-famous concept of the “Banality of Evil.” Using footage from the actual Eichmann trial and weaving a narrative that spans three countries, von Trotta beautifully turns the often invisible passion for thought into immersive, dramatic cinema. An Official Selection at the Toronto International and New York Jewish Film Festivals, Hannah Arendt also co-stars Klaus Pohl as philosopher Martin Heidegger, Nicolas Woodeson as New Yorker editor William Shawn, and two-time Oscar Nominee Janet McTeer (Albert Nobbs) as novelist Mary McCarthy.</span></p>
